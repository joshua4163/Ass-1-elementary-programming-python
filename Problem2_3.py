# Problem 2.3 (Convert feet into meters)

'''
Description:
Step 1: Reads the number in feet from the user
Step 2: calculates and converts to meters
        one foot = 0.305 meters
Step 3: Display the results
'''

# step 1
feet = eval(input("Enter the number in feet: "))

# step 2
meters = feet * 0.305

# step 3
print(feet, "feet is", meters, "meters")
