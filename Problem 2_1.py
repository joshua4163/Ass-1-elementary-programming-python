# Problem 2.1 (Convert Celsius to Fahrenheit)

'''
Description:
Step 1: Read a Celsius degree from console input
Step 2: Convert it into Fahrenheit
        Fahrenheit = (9 /5) * Celsius + 32
Step 3: Display the results
'''

# Step 1
celsius = eval(input("Enter your celsius degrees: "))

# Step 2
fahrenheit = (9 / 5) * celsius + 32

# Step 3
print(celsius, "Celsius is", fahrenheit, "Fahrenheit")
