# Problem 2.4 (Convert pounds into kilograms)

'''
Description:
Step 1: Reads the number in pounds from the user
Step 2: calculates and converts to kilograms
        one pound = 0.454 kilograms
Step 3: Display the results
'''

# step 1
pounds = eval(input("Enter the number in pounds: "))

# step 2
kilograms = pounds * 0.454

# step 3
print(pounds, "pounds is", kilograms, "kilograms")
